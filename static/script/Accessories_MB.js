function showMB(brand, socket) {
    var Buttons_mbDiv = document.getElementById("Buttons_mb");
    Buttons_mbDiv.innerHTML = "";
    Buttons_mbDiv.classList.add("buttons-mb-container"); // Добавляем класс для контейнера

    Object.keys(mbData[brand][socket]).forEach(chipset => {
        var btn = document.createElement("button_mb");
        btn.className = "btn_cpu";
        btn.textContent = chipset;
        btn.onclick = function() { showMBmodel(brand, socket, chipset); };
        Buttons_mbDiv.appendChild(btn);
    });
}

function showMBmodel(brand, socket, chipset) {
    var Info_mbDiv = document.getElementById("Buttons_mb");
    Info_mbDiv.innerHTML = "";
    Info_mbDiv.classList.add("buttons-mb-container"); // Добавляем класс для контейнера

    Object.keys(mbData[brand][socket][chipset]['manufacturer']).forEach(manufacturer => {
        var btn = document.createElement("button_mb");
        btn.className = "btn_cpu";
        btn.textContent = manufacturer;
        btn.onclick = function() { showMBprice(brand, socket, chipset, manufacturer); };
        Info_mbDiv.appendChild(btn);
    });

}

function showMBprice(brand, socket, chipset, manufacturer) {
    var price = mbData[brand][socket][chipset]["manufacturer"][manufacturer]["micro-atx"]["price"];
    var Info_mbDiv = document.getElementById("Buttons_mb");
    Info_mbDiv.innerHTML = "";
    document.getElementById("selectedText1_mb").textContent = manufacturer.toUpperCase() + ' ' + chipset.toUpperCase();
    document.getElementById("selectedText2_mb").textContent = `${price} ₽`;
}
